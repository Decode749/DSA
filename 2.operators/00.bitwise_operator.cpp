#include<iostream>
using namespace std;
int main()
{
    int a = 4;
    int b = 6;
    // and operator  
    cout<<" a&b : "<<(a&b)<<endl;

    // or operator  
    cout<<" a|b : "<<(a|b)<<endl;

    // not operator  
    cout<<" ~a : "<<(~a)<<endl;

    // xor operator 
    cout<<" a^b : "<<(a^b)<<endl;

    // right shift 
    cout<<(17>>2)<<endl;

    // left shift 
    cout<<(17<<2)<<endl;

}